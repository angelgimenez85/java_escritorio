    private void btnBuscarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarClienteActionPerformed
        frmBusquedaCliente miBusqueda = new frmBusquedaCliente(null, closable);
        miBusqueda.setDatos(misDatos);
        //Centramos el formulario en la pantalla (no en ventana)
        miBusqueda.setLocationRelativeTo(null);
        miBusqueda.setVisible(true);
        //Capturamos el cliente seleccionado
        String respuesta = miBusqueda.getRespuesta();
        if (respuesta.equals("")) {
            return;
        }
        //Buscamos en el combobox el cliente devuelto
        for (int i = 0; i < cmbCliente.getItemCount(); i++) {
            if (((Opcion)cmbCliente.getItemAt(i)).getValor().equals(respuesta)) {
                cmbCliente.setSelectedIndex(i);
                return;
            }
        }
    }//GEN-LAST:event_btnBuscarClienteActionPerformed

