    private void formInternalFrameOpened(javax.swing.event.InternalFrameEvent evt) {//GEN-FIRST:event_formInternalFrameOpened
        try {
            //Establecemos el número de factura
            txtNumFactura.setText("0001-" + Utilidades.agregarCeros(""
                    + (misDatos.getNumFactura()), 8));
            
            //Cargamos los clientes
            Opcion opcion;
            opcion = new Opcion("NA", "Seleccione un cliente...");
            cmbCliente.addItem(opcion);
            ResultSet rs = misDatos.getClientes();
            
            while (rs.next()) {
                opcion = new Opcion(
                    rs.getString("idCliente"),
                    rs.getString("nombres") + " "
                    + rs.getString("apellidos"));
                
                cmbCliente.addItem(opcion);
            }
            
            //Cargamos los productos
            opcion = new Opcion("NA", "Seleccione un producto...");
            cmbProducto.addItem(opcion);
            ResultSet rs2 = misDatos.getProductos();
            
            while (rs2.next()) {
                opcion = new Opcion(
                        rs2.getString("idProducto"),
                        rs2.getString("descripcion"));
                
                cmbProducto.addItem(opcion);
            }
            
            txtFecha.setText(Utilidades.dateToString(new Date()));
            txtTotalCantidad.setText("0");
            txtTotalValor.setText("0");
            llenarTabla();
            
        } catch (SQLException ex) {
            Logger.getLogger(frmFactura.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_formInternalFrameOpened

    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        try {
            if (cmbProducto.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(rootPane, "Seleccione un producto");
                cmbProducto.requestFocusInWindow();
                return;
            }
            if (txtCantidad.getText().equals("")) {
                JOptionPane.showMessageDialog(rootPane,
                        "Debe ingresar una cantidad");
                txtCantidad.requestFocusInWindow();
                return;
            }
            
            if (!Utilidades.isNumeric(txtCantidad.getText())) {
                JOptionPane.showMessageDialog(rootPane,
                        "Debe ingresar una cantidad numérica");
                txtCantidad.setText("");
                txtCantidad.requestFocusInWindow();
                return;
            }
            
            int cantidad = Integer.parseInt(txtCantidad.getText());
            
            if (cantidad <= 0) {
                JOptionPane.showMessageDialog(rootPane,
                        "Debe ingresar un valor mayor a cero");
                txtCantidad.setText("");
                txtCantidad.requestFocusInWindow();
                return;
            }
            
            //Buscamos los datos del producto seleccionado
            ResultSet rs = misDatos.getProducto(
                    ((Opcion) cmbProducto.getSelectedItem()).getValor());
            
            if (rs.next()) {
                //Adicionamos el producto a la tabla
                String registro[] = new String[5];
                int precio = rs.getInt("precio");
                int valor = precio * cantidad;
                registro[0] = rs.getString("idProducto");
                registro[1] = rs.getString("descripcion");
                registro[2] = "" + precio;
                registro[3] = "" + cantidad;
                registro[4] = "" + valor;
                miTabla.addRow(registro);
                cmbProducto.setSelectedIndex(0);
                txtCantidad.setText("");
                cmbProducto.requestFocusInWindow();
                //Actualizamos los totales
                totales();
            }
        } catch (SQLException ex) {
            Logger.getLogger(frmFactura.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
        if (cmbCliente.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Seleccione un cliente");
            cmbCliente.requestFocusInWindow();
            return;
        }

        int totalCantidad = Integer.parseInt(txtTotalCantidad.getText());
        if (totalCantidad == 0) {
            JOptionPane.showMessageDialog(rootPane,
                    "Debe ingresar detalle de la factura");
            cmbProducto.requestFocusInWindow();
            return;
        }

        int respuesta = JOptionPane.showConfirmDialog(rootPane,
                "¿Está seguro de grabar la factura?");
        if (respuesta != 0) {
            return;
        }

        //Grabamos la factura

            
        //Obtenemos el número de factura anterior
        int numFactura = misDatos.getNumFactura();

        //Grabamos el encabezado de la factura
        misDatos.agregarFactura(
                numFactura, 
                ((Opcion) cmbCliente.getSelectedItem()).getValor(), 
                Utilidades.stringToDate(txtFecha.getText()));

        //Grabamos el detalle de la factura
        int num = tblDetalle.getRowCount();
        //Por cada línea de detalle, grabamos una línea en la BD
        for (int i = 0; i < num; i++) {
            misDatos.agregarDetalleFactura(
                    numFactura,
                    i,
                    Utilidades.objectToString(tblDetalle.getValueAt(i, 0)),
                    Utilidades.objectToString(tblDetalle.getValueAt(i, 1)),
                    Utilidades.objectToInt(tblDetalle.getValueAt(i, 2)),
                    Utilidades.objectToInt(tblDetalle.getValueAt(i, 3)));
        }

        //Inicializamos los campos
        cmbCliente.setSelectedIndex(0);
        limpiarTabla();
        totales();
        cmbCliente.requestFocusInWindow();
        JOptionPane.showMessageDialog(rootPane,
                "Factura " + numFactura + " generada con éxito.");

        //Establecemos el número de factura para un nuevo ingreso
        txtNumFactura.setText("0001-" + Utilidades.agregarCeros("" 
                + (misDatos.getNumFactura()), 8));

    }//GEN-LAST:event_btnGrabarActionPerformed

