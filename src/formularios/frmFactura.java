    private void llenarTabla() {
        String titulos[] = {"ID Producto", "Descripción", "Precio", "Cantidad",
            "Valor"};
        String registro[] = new String[5];
        miTabla = new DefaultTableModel(null, titulos);
        tblDetalle.setModel(miTabla);

        //Alinear campos numéricos a la derecha
        DefaultTableCellRenderer dtcr = new DefaultTableCellRenderer();
        dtcr.setHorizontalAlignment(SwingConstants.RIGHT);
        tblDetalle.getColumnModel().getColumn(2).setCellRenderer(dtcr);
        tblDetalle.getColumnModel().getColumn(3).setCellRenderer(dtcr);
        tblDetalle.getColumnModel().getColumn(4).setCellRenderer(dtcr);
    }

    private void totales() {

        int sumaCantidad = 0;
        int sumaValor = 0;
        int num = tblDetalle.getRowCount();
        for (int i = 0; i < num; i++) {
            sumaCantidad += Utilidades.objectToInt(tblDetalle.getValueAt(i, 3));
            sumaValor += Utilidades.objectToInt(tblDetalle.getValueAt(i, 4));
        }
        txtTotalCantidad.setText("" + sumaCantidad);
        txtTotalValor.setText("" + sumaValor);
    }

    public void limpiarTabla() {
        try {
            DefaultTableModel modelo = (DefaultTableModel) tblDetalle.getModel();
            int filas = tblDetalle.getRowCount();
            for (int i = 0; filas > i; i++) {
                modelo.removeRow(0);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

